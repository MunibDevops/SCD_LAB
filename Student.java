    private void RegisterActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_RegisterActionPerformed

       
        String name=Name.getText();
        String regno=Regno.getText();
        String pass=Pass.getText();
        String Cpass= cpass.getText();
    
        PreparedStatement ps;
        ResultSet rs;
        String Query="INSERT INTO 'registration'('Name','Regno','Pass') VALUES (?,?,?)";
        
        try {
            
            ps=DBcon.getInstance().prepareStatement(Query);
            ps.setString(1, name);
            ps.setString(2, regno);
            ps.setString(3, pass);
            
            if(ps.executeUpdate()!=0)
            {
                JOptionPane.showMessageDialog(null, "Your account has been created");
                
            }
            else
            {
                JOptionPane.showMessageDialog(null, "Error while creating");
                
            }
            
        } catch (SQLException ex) {
            Logger.getLogger(Student.class.getName()).log(Level.SEVERE, null, ex);
        }
             
    }//GEN-LAST:event_RegisterActionPerformed

