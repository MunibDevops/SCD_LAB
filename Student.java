    private void LoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_LoginActionPerformed
      
      PreparedStatement st;
      ResultSet rs;
       String regno= LRegno.getText();
      String password=LPassword.getText();
      
      String query="Select * FROM REGISTRATION WHERE 'Regno' = ? AND 'Pass'=?";
        try {
            st = DBcon.getInstance().prepareStatement(query);
            st.setString(1, regno);
            st.setString(2, password);
            rs=st.executeQuery();
      
       if(rs.next())
      {
          System.out.println("Login successsful");
          JOptionPane.showMessageDialog(null, "Login Successful");

      }
      else
      {
          JOptionPane.showMessageDialog(null, "Invalid regno/password", "Login error", 2);
          
      }
              
              
            
        } catch (SQLException ex) {
            Logger.getLogger(Student.class.getName()).log(Level.SEVERE, null, ex);
        }
      ;
      
      
    }//GEN-LAST:event_LoginActionPerformed

